def area(a):
	'''
	Returns the area of the square (The squared square side)

	Parameters:
		a (float): The square side
	Return value:
		area (float): The area of the square
	'''
	return a * a


def perimeter(a):
	'''
	Returns the perimeter of the square (The product of 4 and square side)

	Parameters:
		a (float): The square side
	Return value:
		perimeter (float): The perimeter of the square
	'''
	return 4 * a